package com.tgt.igniteplus;

//iterating and displaying array elements using while loop

public class while_loop {
        public static void main(String args[]){
            int arr[]={2,11,45,9};
            int i=0;
            while(i<4){
                System.out.println(arr[i]);
                i++;
            }
        }
    }

/*output = 2
          11
          45
          9 */
